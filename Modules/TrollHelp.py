    async def make_log(self):
        async for d in self.client.iter_dialogs():
            if d.name == self.strings["deleted_log"]:
                self.del_chat = d.id
                return
        ch = await self.client(CreateChannelRequest(self.strings["deleted_log"], ""))
        self.del_chat = ch.chats[0].id

    async def new_msg(self, m):
        if not await self.check_sub():
            await m.reply(self.strings["need_sub"])
            return
        if not m.is_private: return
        key = (m.chat_id, m.id)
        sender = await m.get_sender()
        name = utils.get_display_name(sender)
        self.cache[key] = {"text": m.raw_text or "(–º–µ–¥–∏–∞)", "name": name, "date": m.date}
        if m.sender_id in self.bl:
            await m.delete()
            return
        if self.antisteal:
            text = (m.raw_text or "").lower()
            for t in self.threats:
                if t.lower() in text:
                    await self.client.send_message("me", f"—É–≥—Ä–æ–∑–∞ –æ—Ç {name}: {text[:200]}")
                    await m.delete()
                    return

    async def del_msg(self, e):
        if not e.is_private: return
        for id in e.deleted_ids:
            key = (e.chat_id, id)
            if key in self.cache:
                data = self.cache.pop(key)
                log = f"{data['date']} | {data['name']} | –ª—Å | {data['text']}"
                await self.client.send_message(self.del_chat, log)
                await self.client.send_message("me", f"—É–¥–∞–ª–∏–ª {data['name']}: {data['text']}")

